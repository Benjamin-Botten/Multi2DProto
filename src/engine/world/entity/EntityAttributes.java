package engine.world.entity;

public class EntityAttributes {	
	public final int life;
	public final int love;
	public final int noa;
	public final int spirit;
	public final int crit;
	public final int power;
	
	public EntityAttributes(int life, int love, int noa, int spirit, int crit, int power) {
		this.life = life;
		this.love = love;
		this.noa = noa;
		this.spirit = spirit;
		this.crit = crit;
		this.power = power;
	}
}
